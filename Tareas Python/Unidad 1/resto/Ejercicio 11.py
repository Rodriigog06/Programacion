caracter = input ("dime una letra :").lower()
if caracter == "a" :
    print ("es la primera vocal (A)")
elif caracter == "e" :
    print ("es la segunda vocal (E)")
elif caracter == "i" :
    print ("es la tercera vocal (I)")
elif caracter == "o" :
    print ("es la cuarta vocal (O)")
elif caracter == "u" :
    print ("es la quinta vocal (U)")
else: 
    print ("no es una vocal")
